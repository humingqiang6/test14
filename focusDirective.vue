<template>
  <div>
    <h1>Vue 自定义指令示例</h1>
    <input v-focus type="text" placeholder="这个输入框应该自动获得焦点">
    <br><br>
    <input type="text" placeholder="这个不会">
  </div>
</template>

<script>
export default {
  name: 'FocusDirectiveExample',
  directives: {
    // 定义一个名为 'focus' 的局部自定义指令
    focus: {
      // 在元素挂载到 DOM 时触发
      mounted(el) {
        // 聚焦元素
        el.focus();
      }
    }
  }
}
</script>

<style>
input {
  padding: 8px;
  margin: 4px;
}
</style>