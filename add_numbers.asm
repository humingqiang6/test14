section .text
    global _start

; Функция для сложения двух чисел
; Принимает два аргумента: rdi (первое число), rsi (второе число)
; Возвращает результат в rax
add_numbers:
    mov rax, rdi    ; Копируем первое число в rax
    add rax, rsi    ; Прибавляем второе число
    ret             ; Возвращаем результат в rax

_start:
    ; Пример использования функции
    ; Сложим 5 и 7
    mov rdi, 5      ; Первое число
    mov rsi, 7      ; Второе число
    call add_numbers ; Вызов функции

    ; Результат теперь в rax (должен быть 12)

    ; Выход из программы
    ; Используем системный вызов sys_exit
    mov rdi, 0      ; Код возврата 0
    mov rax, 60     ; Номер системного вызова для sys_exit
    syscall