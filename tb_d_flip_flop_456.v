`timescale 1ns / 1ps

module tb_d_flip_flop;

    // Объявление сигналов
    reg clk;
    reg rst_n;
    reg d;
    wire q;

    // Инстанцирование тестируемого модуля (D-триггер)
    d_flip_flop uut (
        .clk(clk),
        .rst_n(rst_n),
        .d(d),
        .q(q)
    );

    // Генерация тактового сигнала (период 10 нс)
    initial begin
        clk = 0;
        forever #5 clk = ~clk; // Переключение каждые 5 нс
    end

    // Основной процесс тестирования
    initial begin
        // Инициализация сигналов
        rst_n = 0; // Активный уровень сброса
        d = 0;
        #10; // Ждем 10 нс

        // Деактивация сброса
        rst_n = 1;
        #15; // Ждем немного после сброса

        // Тестирование: подача различных значений на D
        $display("Тестирование D-триггера");
        $monitor("Время: %0t, D: %b, Q: %b", $time, d, q);

        d = 1; #20; // Устанавливаем D в 1 и ждем 20 нс (2 такта)
        d = 0; #20; // Устанавливаем D в 0 и ждем 20 нс (2 такта)
        d = 1; #20; // Устанавливаем D в 1 и ждем 20 нс (2 такта)
        d = 0; #20; // Устанавливаем D в 0 и ждем 20 нс (2 такта)

        // Повторный сброс
        rst_n = 0; #15;
        rst_n = 1;

        #20; // Дополнительное время для наблюдения

        $display("Тестирование завершено.");
        $finish; // Завершение симуляции
    end

endmodule