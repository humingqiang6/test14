section .text
    global _start

_start:
    ; Загружаем два числа в регистры
    mov eax, 5      ; Первое число
    mov ebx, 3      ; Второе число

    ; Складываем числа
    add eax, ebx    ; Результат сложения в eax

    ; Загружаем результат в ecx для системного вызова (например, для вывода или проверки)
    ; В реальном приложении здесь был бы код для вывода результата или его использования
    ; Для простоты просто сохраним результат в регистр ecx
    mov ecx, eax

    ; Выход из программы (пример для Linux)
    mov eax, 1      ; Номер системного вызова sys_exit
    int 0x80        ; Вызов ядра