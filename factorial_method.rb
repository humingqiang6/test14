# Метод для вычисления факториала числа
def factorial(n)
  # Проверяем, является ли число неотрицательным целым
  if n < 0
    raise ArgumentError, "Факториал не определен для отрицательных чисел"
  end

  # Базовые случаи
  return 1 if n == 0 || n == 1

  # Вычисляем факториал с помощью итерации
  result = 1
  (2..n).each do |i|
    result *= i
  end
  result
end

# Пример использования
puts factorial(5) # Выведет 120